package com.xx.entity;

public class EmployeeWork {
	
	private Integer employeeWorkId;
	
	private Integer employeeId;
	
	private Integer employeeName;
	
	private String time;
	
	private String cargo;
	
	private String workType;
	
	private double quantity;
	
	private Integer commission;
	
	private Integer workId;

	public Integer getEmployeeWorkId() {
		return employeeWorkId;
	}

	public void setEmployeeWorkId(Integer employeeWorkId) {
		this.employeeWorkId = employeeWorkId;
	}

	public Integer getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(Integer employeeId) {
		this.employeeId = employeeId;
	}

	public Integer getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(Integer employeeName) {
		this.employeeName = employeeName;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getCargo() {
		return cargo;
	}

	public void setCargo(String cargo) {
		this.cargo = cargo;
	}

	public String getWorkType() {
		return workType;
	}

	public void setWorkType(String workType) {
		this.workType = workType;
	}

	public double getQuantity() {
		return quantity;
	}

	public void setQuantity(double quantity) {
		this.quantity = quantity;
	}

	public Integer getCommission() {
		return commission;
	}

	public void setCommission(Integer commission) {
		this.commission = commission;
	}

	public Integer getWorkId() {
		return workId;
	}

	public void setWorkId(Integer workId) {
		this.workId = workId;
	}
	
}
